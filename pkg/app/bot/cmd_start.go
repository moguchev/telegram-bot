func (b *bot) StartCmd(upd tgbotapi.Update) {
	b.sendMessage(upd.Message.Chat.ID, startMsg, true)

	ch, ok := b.chats.GetChat(ChatID(upd.Message.Chat.ID))
	if !ok {
		return
	}

	if ch.GetToken() == "" {
		b.sendMessage(upd.Message.Chat.ID, noAPIKeyMsg, true)
	}
}
